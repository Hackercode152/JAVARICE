public class increment {
    public static void main(String[] args) {
        System.out.println(
             "Incrementing an integer");
        int x = 3;
        int y = 11;
        System.out.println(" initial value of x:  + ++x ");
        System.out.println(++x * y--); 
         System.out.println(++x - y--);    
    }
}
